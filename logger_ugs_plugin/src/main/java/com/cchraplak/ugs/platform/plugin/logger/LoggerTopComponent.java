    @Override
    public void componentOpened() {
        // TODO add custom code on component opening
        
        try {
        
            File saveDir = new File(dirFile);

            if (saveDir.exists()) {
                System.out.println("Found Init File");
                Scanner saveScanner = new Scanner(saveDir);

                while (saveScanner.hasNextLine()) {
                    String data = saveScanner.nextLine();
                    directoryString = data;
                    System.out.println(data);
                }
                saveScanner.close();
            }
            else {
                System.out.println("No Init File");
                writeRoot();
            }
        }
        catch (IOException e) {
            System.out.println("=====================================================");
            System.out.println("ERROR NO FILE FOUND!!!");
            System.out.println("=====================================================");
            e.printStackTrace();
        }
        
        dirLoaded.setText(outDir());
    }

